class Dac {

	public:
		Dac(uint32_t DAC_CHANNEL, uint16_t angle){
			set_const_sin();
			get_sin(angle);
			switch (DAC_CHANNEL){
				case 1:
					HAL_TIM_Base_Start(&htim6);
					HAL_DAC_Start_DMA(&hdac,DAC_CHANNEL_1,(uint32_t *)_SIN_TAB_const,PMSM_SINTABLESIZE,DAC_ALIGN_12B_R);
					break;
				case 2:
					HAL_TIM_Base_Start(&htim7);
					HAL_DAC_Start_DMA(&hdac,DAC_CHANNEL_2,(uint32_t *)_SIN_TAB,PMSM_SINTABLESIZE,DAC_ALIGN_12B_R);
					break;
			}
		
		};
		void change_angle(uint16_t angle){
			get_sin(angle);
		}
		
	
	private:
		uint16_t _SIN_TAB[PMSM_SINTABLESIZE];
		uint16_t _SIN_TAB_const[PMSM_SINTABLESIZE];
		void get_sin(uint16_t number){
			for (int i = 0; i<PMSM_SINTABLESIZE; i++){
				_SIN_TAB[i] = SIN_TAB[i][number];
			}
		}
		void set_const_sin(){
			for (int i = 0; i<PMSM_SINTABLESIZE; i++){
				_SIN_TAB_const[i] = SIN_TAB[i][0];
			}
		}
		
		
};

